// Package gengatewaybindings renders the gateway-API App.Get<Kind> bindings from
// the single resource-identity source (resourcecontract.BuiltinResources).
//
// These bindings are pure boilerplate: every part that varies per kind — the
// kind name and whether it is namespaced or cluster-scoped — already lives in
// the contract. The DTO type is <Kind>Details, the service method is <Kind>, and
// the service constructor is fixed for the group. Generating them from the
// contract makes "add a gateway kind" a single contract entry: nothing to copy,
// nothing to drift. This is the registry-driven approach proven on one subsystem.
package gengatewaybindings

import (
	"bytes"
	"fmt"
	"go/format"
	"sort"

	"github.com/luxury-yacht/app/backend/resourcecontract"
)

const gatewayGroup = "gateway.networking.k8s.io"

// Render returns the gofmt'd source of the generated gateway bindings file.
func Render() ([]byte, error) {
	var kinds []resourcecontract.BuiltinResource
	for _, r := range resourcecontract.BuiltinResources {
		if r.Group == gatewayGroup {
			kinds = append(kinds, r)
		}
	}
	sort.Slice(kinds, func(i, j int) bool { return kinds[i].Kind < kinds[j].Kind })

	var b bytes.Buffer
	b.WriteString("// Code generated by gengatewaybindings; DO NOT EDIT.\n\n")
	b.WriteString("package backend\n\n")
	b.WriteString("import \"github.com/luxury-yacht/app/backend/resources/gatewayapi\"\n\n")
	for _, r := range kinds {
		writeBinding(&b, r)
	}
	return format.Source(b.Bytes())
}

func writeBinding(b *bytes.Buffer, r resourcecontract.BuiltinResource) {
	dto := r.Kind + "Details"
	if r.Namespaced {
		fmt.Fprintf(b, `func (a *App) Get%[1]s(clusterID, namespace, name string) (*%[2]s, error) {
	deps, selectionKey, err := a.resolveClusterDependencies(clusterID)
	if err != nil {
		return nil, err
	}
	return FetchNamespacedResource(a, deps, selectionKey, "%[1]s", namespace, name, func() (*%[2]s, error) {
		return gatewayapi.NewService(deps).%[1]s(namespace, name)
	})
}

`, r.Kind, dto)
		return
	}
	fmt.Fprintf(b, `func (a *App) Get%[1]s(clusterID, name string) (*%[2]s, error) {
	deps, selectionKey, err := a.resolveClusterDependencies(clusterID)
	if err != nil {
		return nil, err
	}
	return FetchClusterResource(a, deps, selectionKey, "%[1]s", name, func() (*%[2]s, error) {
		return gatewayapi.NewService(deps).%[1]s(name)
	})
}

`, r.Kind, dto)
}
