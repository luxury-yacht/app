type generatedPermission struct {
	ID           string  `json:"id"`
	Slot         string  `json:"slot"`
	Verb         string  `json:"verb"`
	Group        *string `json:"group,omitempty"`
	Version      string  `json:"version,omitempty"`
	ResourceKind string  `json:"resourceKind,omitempty"`
	Subresource  string  `json:"subresource,omitempty"`
	Namespace    bool    `json:"namespace,omitempty"`
	Name         bool    `json:"name,omitempty"`
}

type generatedDefinition struct {
	Label              string                       `json:"label"`
	BackendAction      string                       `json:"backendAction,omitempty"`
	PayloadFields      *[]objectaction.PayloadField `json:"payloadFields,omitempty"`
	Permission         *generatedPermission         `json:"permission,omitempty"`
	FrontendPermission string                       `json:"frontendPermission,omitempty"`
	BackendPermission  string                       `json:"backendPermission,omitempty"`
	DeniedReason       string                       `json:"deniedReason,omitempty"`
}

type kindCapability struct {
	Kind                string   `json:"kind"`
	Group               string   `json:"group"`
	Version             string   `json:"version"`
	Aliases             []string `json:"aliases"`
	Restart             bool     `json:"restart,omitempty"`
	Rollback            bool     `json:"rollback,omitempty"`
	Scale               bool     `json:"scale,omitempty"`
	PortForward         bool     `json:"portForward,omitempty"`
	Reconnect           bool     `json:"reconnect,omitempty"`
	UsesServicePortSpec bool     `json:"usesServicePortSpec,omitempty"`
	Trigger             bool     `json:"trigger,omitempty"`
	Suspend             bool     `json:"suspend,omitempty"`
	Cordon              bool     `json:"cordon,omitempty"`
	Drain               bool     `json:"drain,omitempty"`
}

type generatedCatalog struct {
	IDs              map[string]string                `json:"ids"`
	Actions          map[string]string                `json:"actions"`
	MutatingIDs      []string                         `json:"mutatingIds"`
	Definitions      map[string]generatedDefinition   `json:"definitions"`
	KindCapabilities map[string]kindCapability        `json:"kindCapabilities"`
	NodePermissions  []map[string]generatedPermission `json:"nodePermissions"`
}

func permissionValue(permission *objectaction.PermissionTemplate) *generatedPermission {
	if permission == nil {
		return nil
	}
	return &generatedPermission{
		ID: permission.ID, Slot: permission.Slot, Verb: permission.Verb,
		Group: permission.Group, Version: permission.Version, ResourceKind: permission.ResourceKind,
		Subresource: permission.Subresource, Namespace: permission.Namespace, Name: permission.Name,
	}
}

func kindCapabilities() []kindCapability {
	capabilities := make([]kindCapability, 0)
	for _, descriptor := range kindregistry.All {
		workload := descriptor.Workload
		capability := kindCapability{
			Kind: descriptor.Identity.Kind, Group: descriptor.Identity.Group, Version: descriptor.Identity.Version,
			Aliases:  append([]string{descriptor.Identity.Kind}, descriptor.Actions.Aliases...),
			Restart:  workload != nil && workload.Restart != nil,
			Rollback: workload != nil && workload.RevisionHistory != nil && workload.ApplyPodTemplate != nil,
			Scale:    workload != nil && workload.Scale != nil, PortForward: descriptor.PortForward != nil,
			Trigger: descriptor.Actions.Trigger, Suspend: descriptor.Actions.Suspend,
			Cordon: descriptor.Actions.Cordon, Drain: descriptor.Actions.Drain,
		}
		if descriptor.PortForward != nil {
			capability.Reconnect = descriptor.PortForward.Reconnect
			capability.UsesServicePortSpec = descriptor.PortForward.UsesServicePortSpec
		}
		if len(capability.Aliases) > 1 || capability.Restart || capability.Rollback || capability.Scale || capability.PortForward || capability.Trigger || capability.Suspend || capability.Cordon || capability.Drain {
			capabilities = append(capabilities, capability)
		}
	}
	sort.Slice(capabilities, func(i, j int) bool { return capabilities[i].Kind < capabilities[j].Kind })
	return capabilities
}

// Render generates a compact TypeScript catalog from backend-owned definitions.
func Render() ([]byte, error) {
	catalog := generatedCatalog{
		IDs: make(map[string]string), Actions: make(map[string]string),
		Definitions:      make(map[string]generatedDefinition),
		KindCapabilities: make(map[string]kindCapability),
	}
	for _, definition := range objectaction.Definitions {
		id := string(definition.ID)
		catalog.IDs[definition.Key] = id
		generated := generatedDefinition{Label: definition.Label}
		if definition.BackendAction != "" {
			fields := append([]objectaction.PayloadField{}, definition.PayloadFields...)
			generated.BackendAction = definition.BackendAction
			generated.PayloadFields = &fields
			generated.Permission = permissionValue(definition.Permission)
			generated.FrontendPermission = definition.FrontendPermission
			generated.BackendPermission = definition.BackendPermission
			generated.DeniedReason = definition.DeniedReason
			catalog.MutatingIDs = append(catalog.MutatingIDs, id)
		}
		catalog.Definitions[id] = generated
	}
	for _, definition := range objectaction.FrontendBackendActions {
		catalog.Actions[definition.Key] = definition.Action
	}
	for _, capability := range kindCapabilities() {
		catalog.KindCapabilities[capability.Kind] = capability
	}
	for index := range objectaction.NodePermissions {
		catalog.NodePermissions = append(catalog.NodePermissions, map[string]generatedPermission{
			"permission": *permissionValue(&objectaction.NodePermissions[index]),
		})
	}
	encoded, err := json.Marshal(catalog)
	if err != nil {
		return nil, err
	}
	return append(append([]byte(generatedHeader+"const catalog = "), encoded...), []byte(" as const;"+generatedTypes)...), nil
}
